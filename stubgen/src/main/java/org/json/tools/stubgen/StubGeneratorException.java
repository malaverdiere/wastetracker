package org.json.tools.stubgen;

/**
 * @author martind
 *
 */
public class StubGeneratorException extends Exception {

	private static final long serialVersionUID = -8281645377614369676L;

	public StubGeneratorException() {
		super();
	}

	public StubGeneratorException(String message) {
		super(message);
	}

	public StubGeneratorException(Throwable cause) {
		super(cause);
	}

	public StubGeneratorException(String message, Throwable cause) {
		super(message, cause);
	}
}
